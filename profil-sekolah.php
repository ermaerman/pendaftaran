            <p align="justify">
              <br>
            <b>SMK Mandalahayu II Bekasi</b> didirikan oleh bapak Pembina Yayasan yaitu DRS.H.E. Achmad Lajuardinour.MM.Phd. pada tanggal 20 Mei 2007. Hal ini dimaksudkan untuk menampung para siswa/i yang ingin bersekolah di Yayasan Mandalahayu, terutama dengan jarak tempat yang lebih dekat dari rumahnya.

            <br>
            <br>

            Dibangun di atas tanah seluas 5200 m2 yang terletak di jalan Pengasinan Tengah No. 99 Pengasinan-Rawalumbu, Bekasi.
            Sekolah pertama yang didirikan adalah SMK walaupun untuk SMP dan SMA, di Yayasan sudah mendapat izin dari Dinas Pendidikan kota Bekasi, namun seiring berjalannya waktu, peminat untuk ke SMP dan SMA hanya sedikit sedangkan untuk unit SMK terhitung banyak.

            <br>
            <br>

            Demikian profil singkat SMK Mandalahayu II. Semoga di tahun yang akan datang segala sesuatu yang mengarah kepada kemajuan di SMK Mandalahayu II bisa terlaksana dan tercapai.
